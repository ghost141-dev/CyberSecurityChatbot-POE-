        // Shared click handler for all 10 topic buttons in the left sidebar.
        // Each button's Tag property holds the topic keyword (e.g. "phishing").
        // Clicking a button behaves exactly like typing that keyword and pressing Send.
        private void topic_button_Click(object sender, RoutedEventArgs e)
        {
            // Cast the generic sender to Button and read its Tag
            if (sender is Button btn && btn.Tag is string topic)
            {
                // Show the topic keyword as if the user typed it
                ShowMessage(_chatbot.UserName, topic);

                // Get the response from the engine (same path as manual typing)
                EngineResponse response = _chatbot.ProcessInput(topic);

                ShowMessage("Bot", response.Message);
                _chatbot.IncrementMessages();
                RefreshMemoryPanel();
            }
        }

        // ── Part 3: Top-level navigation (Chat / Tasks / Quiz / Log) ──


        /// Shared click handler for the four sidebar navigation buttons.
        // Each button's Tag identifies which panel to show: "chat", "tasks",
        // "quiz", or "log". Exactly one of the four panel grids is Visible
        // at a time — the rest are Hidden, mirroring the existing
        // home/username/chat screen-switching pattern.
        // </summary>
        private void nav_button_Click(object sender, RoutedEventArgs e)
        {
            if (!(sender is Button btn && btn.Tag is string target)) return;

            // Hide all four panels, then show only the requested one.
            chat_panel_grid.Visibility  = Visibility.Hidden;
            tasks_panel_grid.Visibility = Visibility.Hidden;
            quiz_panel_grid.Visibility  = Visibility.Hidden;
            log_panel_grid.Visibility   = Visibility.Hidden;

            switch (target)
            {
                case "chat":
                    chat_panel_grid.Visibility = Visibility.Visible;
                    question.Focus();
                    break;

                case "tasks":
                    tasks_panel_grid.Visibility = Visibility.Visible;
                    _ = RefreshTaskListAsync();
                    break;

                case "quiz":
                    quiz_panel_grid.Visibility = Visibility.Visible;
                    break;

                case "log":
                    log_panel_grid.Visibility = Visibility.Visible;
                    RefreshActivityLog(showAll: false);
                    break;
            }
        }

        // ── Part 3: Task Assistant panel ───────────────────────────────

        // Click handler for "Add Task". Reads the title and reminder
        // selection from the form, adds the task via TaskAssistantHandler,
        // and refreshes the task list.
        private async void add_task_button_Click(object sender, RoutedEventArgs e)
        {
            string title = new_task_title_input.Text.Trim();
            if (string.IsNullOrWhiteSpace(title))
            {
                MessageBox.Show("Please enter a task title first.", "Task Assistant",
                                 MessageBoxButton.OK, MessageBoxImage.Information);
                return;
            }

            DateTime? reminderDate = GetReminderDateFromCombo();

            var (success, message, _) = await _taskAssistant.AddTaskAsync(title, title, reminderDate);

            if (!success)
                MessageBox.Show(message, "Task Assistant", MessageBoxButton.OK, MessageBoxImage.Warning);

            new_task_title_input.Clear();
            new_task_reminder_combo.SelectedIndex = 0;

            await RefreshTaskListAsync();
        }

        // Translates the reminder ComboBox selection into a concrete
        // DateTime, or null if "No reminder" is selected.
        private DateTime? GetReminderDateFromCombo()
        {
            switch (new_task_reminder_combo.SelectedIndex)
            {
                case 1: return DateTime.Now.AddDays(1);
                case 2: return DateTime.Now.AddDays(3);
                case 3: return DateTime.Now.AddDays(7);
                default: return null; // "No reminder"
            }
        }

        // Reloads the task list from the database and rebuilds the
        // ListView content. Each task row shows its title, description,
        // reminder date (if any), and Complete/Delete buttons.
        private async Task RefreshTaskListAsync()
        {
            _loadedTasks = await _taskAssistant.GetAllTasksAsync();

            tasks_list_view.Items.Clear();

            if (_loadedTasks.Count == 0)
            {
                tasks_list_view.Items.Add(BuildEmptyStateRow(
                    _database.IsAvailable
                        ? "No tasks yet — add one above to get started."
                        : "Task list unavailable — MySQL is not connected."));
                return;
            }

            foreach (TaskItem task in _loadedTasks)
                tasks_list_view.Items.Add(BuildTaskRow(task));
        }


        /// Builds a single visual row for one task: title, description,
        // reminder badge (if set), and Complete/Delete buttons.
        // Built in code (not XAML DataTemplate) for the same reason
        // ShowMessage builds chat bubbles in code — each row needs
        // individualised content and per-row button event wiring.
        // </summary>
        private Border BuildTaskRow(TaskItem task)
        {
            Border row = new Border
            {
                Margin = new Thickness(6, 6, 6, 6),
                Padding = new Thickness(14, 12, 14, 12),
                CornerRadius = new CornerRadius(10),
                Background = task.IsCompleted
                    ? new SolidColorBrush(Color.FromRgb(240, 253, 244))   // pale green = done
                    : new SolidColorBrush(Color.FromRgb(249, 250, 251))  // pale grey = pending
            };

            Grid grid = new Grid();
            grid.ColumnDefinitions.Add(new ColumnDefinition { Width = new GridLength(1, GridUnitType.Star) });
            grid.ColumnDefinitions.Add(new ColumnDefinition { Width = GridLength.Auto });
            grid.ColumnDefinitions.Add(new ColumnDefinition { Width = GridLength.Auto });

            // Left: title + description + reminder
            StackPanel textPanel = new StackPanel { Margin = new Thickness(0, 0, 12, 0) };

            TextBlock titleBlock = new TextBlock
            {
                Text = task.Title,
                FontWeight = FontWeights.SemiBold,
                FontSize = 14,
                TextWrapping = TextWrapping.Wrap,
                Foreground = task.IsCompleted ? Brushes.Gray : Brushes.Black,
                TextDecorations = task.IsCompleted ? TextDecorations.Strikethrough : null
            };
            textPanel.Children.Add(titleBlock);

            if (!string.IsNullOrWhiteSpace(task.Description) && task.Description != task.Title)
            {
                textPanel.Children.Add(new TextBlock
                {
                    Text = task.Description,
                    FontSize = 12,
                    Foreground = Brushes.Gray,
                    TextWrapping = TextWrapping.Wrap,
                    Margin = new Thickness(0, 2, 0, 0)
                });
            }

            if (task.ReminderDate.HasValue)
            {
                textPanel.Children.Add(new TextBlock
                {
                    Text = $"⏰ Reminder: {task.ReminderDate.Value:dd MMM yyyy}",
                    FontSize = 11,
                    Foreground = new SolidColorBrush(Color.FromRgb(37, 99, 235)),
                    Margin = new Thickness(0, 4, 0, 0)
                });
            }

            Grid.SetColumn(textPanel, 0);
            grid.Children.Add(textPanel);

            // Complete button (hidden if already completed)
            if (!task.IsCompleted)
            {
                Button completeBtn = new Button
                {
                    Content = "✓ Complete",
                    Width = 100,
                    Style = (Style)FindResource("PrimaryActionButtonStyle"),
                    Margin = new Thickness(0, 0, 8, 0),
                    Tag = task.Id
                };
                completeBtn.Click += async (s, e) =>
                {
                    var (success, message) = await _taskAssistant.CompleteTaskAsync(task.Id, task.Title);
                    if (!success) MessageBox.Show(message, "Task Assistant");
                    await RefreshTaskListAsync();
                };
                Grid.SetColumn(completeBtn, 1);
                grid.Children.Add(completeBtn);
            }

            // Delete button
            Button deleteBtn = new Button
            {
                Content = "🗑 Delete",
                Width = 90,
                Style = (Style)FindResource("SecondaryActionButtonStyle"),
                Tag = task.Id
            };
            deleteBtn.Click += async (s, e) =>
            {
                var (success, message) = await _taskAssistant.DeleteTaskAsync(task.Id, task.Title);
                if (!success) MessageBox.Show(message, "Task Assistant");
                await RefreshTaskListAsync();
            };
            Grid.SetColumn(deleteBtn, 2);
            grid.Children.Add(deleteBtn);

            row.Child = grid;
            return row;
        }

        /// <summary>Builds a simple grey placeholder row for empty-state lists.</summary>
        private Border BuildEmptyStateRow(string message)
        {
            return new Border
            {
                Padding = new Thickness(16),
                Child = new TextBlock
                {
                    Text = message,
                    Foreground = Brushes.Gray,
                    FontSize = 13,
                    TextWrapping = TextWrapping.Wrap,
                    HorizontalAlignment = HorizontalAlignment.Center
                }
            };
        }

        // ── Part 3: Quiz panel ──────────────────────────────────────────

        // Click handler for "Start Quiz" / "Play Again". Begins a fresh
        // quiz attempt and displays the first question.
        private void start_quiz_button_Click(object sender, RoutedEventArgs e)
        {
            _quiz.StartNewQuiz();
            _activityLog.Log("Quiz started", "Quiz");

            quiz_start_panel.Visibility   = Visibility.Hidden;
            quiz_results_panel.Visibility = Visibility.Hidden;
            quiz_question_panel.Visibility = Visibility.Visible;

            DisplayCurrentQuizQuestion();
        }


        /// Renders the current question's text and answer options as
        // selectable buttons. Called when the quiz starts and after
        // each "Next Question" click.
        private void DisplayCurrentQuizQuestion()
        {
            QuizQuestion q = _quiz.GetCurrentQuestion();
            if (q == null) return;

            _selectedQuizOptionIndex = -1;

            quiz_progress_text.Text = $"Question {_quiz.CurrentIndex + 1} of {_quiz.TotalQuestions}  •  Score: {_quiz.Score}";
            quiz_question_text.Text = q.Text;

            quiz_feedback_text.Visibility = Visibility.Collapsed;
            quiz_next_button.Visibility   = Visibility.Collapsed;

            quiz_options_panel.Children.Clear();

            for (int i = 0; i < q.Options.Count; i++)
            {
                int optionIndex = i; // local copy for closure capture
                Button optionBtn = new Button
                {
                    Content = q.Options[i],
                    Margin = new Thickness(0, 0, 0, 8),
                    Height = 40,
                    HorizontalContentAlignment = HorizontalAlignment.Left,
                    Padding = new Thickness(14, 0, 0, 0),
                    Background = new SolidColorBrush(Color.FromRgb(249, 250, 251)),
                    BorderBrush = new SolidColorBrush(Color.FromRgb(209, 213, 219)),
                    BorderThickness = new Thickness(1),
                    Cursor = System.Windows.Input.Cursors.Hand
                };
                optionBtn.Click += (s, e) => SubmitQuizAnswer(optionIndex, optionBtn);
                quiz_options_panel.Children.Add(optionBtn);
            }
        }

        /// <summary>
        /// Handles the user selecting an answer option: submits it to
        /// QuizEngine, colours the chosen button green/red, shows the
        /// explanation, and reveals the "Next Question" button.
        /// </summary>
        private void SubmitQuizAnswer(int selectedIndex, Button clickedButton)
        {
            // Disable all option buttons after the first answer to prevent re-answering
            foreach (UIElement child in quiz_options_panel.Children)
                if (child is Button b) b.IsEnabled = false;

            var (wasCorrect, explanation) = _quiz.SubmitAnswer(selectedIndex);

            clickedButton.Background = wasCorrect
                ? new SolidColorBrush(Color.FromRgb(220, 252, 231))   // green = correct
                : new SolidColorBrush(Color.FromRgb(254, 226, 226));  // red = incorrect

            quiz_feedback_text.Text = (wasCorrect ? "✅ Correct! " : "❌ Not quite. ") + explanation;
            quiz_feedback_text.Foreground = wasCorrect
                ? new SolidColorBrush(Color.FromRgb(6, 95, 70))
                : new SolidColorBrush(Color.FromRgb(185, 28, 28));
            quiz_feedback_text.Visibility = Visibility.Visible;

            quiz_next_button.Content    = _quiz.IsFinished ? "See Results" : "Next Question";
            quiz_next_button.Visibility = Visibility.Visible;
        }


        // Click handler for "Next Question" / "See Results". Advances
        // to the next question, or shows the final score screen if the
        // quiz is complete.
        private void quiz_next_button_Click(object sender, RoutedEventArgs e)
        {
            if (_quiz.IsFinished)
            {
                quiz_question_panel.Visibility = Visibility.Hidden;
                quiz_results_panel.Visibility  = Visibility.Visible;
                quiz_score_text.Text = _quiz.GetScoreSummary();

                _activityLog.Log($"Quiz completed - scored {_quiz.Score}/{_quiz.TotalQuestions}", "Quiz");
            }
            else
            {
                DisplayCurrentQuizQuestion();
            }
        }

        // ── Part 3: Activity Log panel ──────────────────────────────────

        // Rebuilds the Activity Log ListView from ActivityLogger.
        // <param name="showAll">If true, shows every entry; otherwise the recent few.</param>
        private void RefreshActivityLog(bool showAll)
        {
            log_list_view.Items.Clear();

            List<ActivityLogEntry> entries = showAll ? _activityLog.GetAll() : _activityLog.GetRecent();

            if (entries.Count == 0)
            {
                log_list_view.Items.Add(BuildEmptyStateRow("No actions logged yet this session."));
                log_show_more_button.Visibility = Visibility.Collapsed;
                return;
            }

            foreach (ActivityLogEntry entry in entries)
                log_list_view.Items.Add(BuildLogRow(entry));

            // Only offer "Show more" if there are additional older entries to reveal
            log_show_more_button.Visibility =
                (!showAll && _activityLog.Count > entries.Count) ? Visibility.Visible : Visibility.Collapsed;
        }

        // Builds a single visual row for one activity log entry.
        private Border BuildLogRow(ActivityLogEntry entry)
        {
            return new Border
            {
                Margin = new Thickness(6, 4, 6, 4),
                Padding = new Thickness(12, 10, 12, 10),
                CornerRadius = new CornerRadius(8),
                Background = new SolidColorBrush(Color.FromRgb(249, 250, 251)),
                Child = new TextBlock
                {
                    Text = entry.ToString(),
                    FontSize = 13,
                    TextWrapping = TextWrapping.Wrap,
                    Foreground = Brushes.Black
                }
            };
        }

        // Click handler for "Show Full History".
        private void log_show_more_button_Click(object sender, RoutedEventArgs e)
        {
            RefreshActivityLog(showAll: true);
        }

        // Creates a styled chat bubble and adds it to the message list (chats ListView).
        // Bot bubbles are blue-tinted and left-aligned.
        // User bubbles are green-tinted and right-aligned (achieved via left margin).
        // </summary>
        // <param name="sender">
        //   "Bot" for the bot's replies; the user's name for their messages.
        // </param>
        // <param name="message">The text content of the bubble.</param>
        private void ShowMessage(string sender, string message)
        {
            // Determine the bubble side by checking whether the sender is the bot
            bool isBot = sender == "Bot";

            // ── Bubble container ──────────────────────────────────────
            // Left margin pushes user bubbles to the right (60px indent from left).
            // Right margin pushes bot bubbles away from the right edge.
            Border bubble = new Border
            {
                Margin        = new Thickness(isBot ? 0 : 60, 4, isBot ? 60 : 0, 4),
                Padding       = new Thickness(14, 10, 14, 10),
                CornerRadius  = new CornerRadius(12),
                Background    = isBot
                                    ? new SolidColorBrush(Color.FromRgb(224, 237, 255))  // light blue
                                    : new SolidColorBrush(Color.FromRgb(220, 252, 231)), // light green
                BorderThickness = new Thickness(0)
            };

            // ── Text block with two Runs ──────────────────────────────
            // Run 1: sender label in bold (with emoji prefix)
            // Run 2: message body in normal weight
            // Using Inlines instead of two separate TextBlocks keeps the
            // label and message in the same wrapping flow.
            TextBlock tb = new TextBlock
            {
                TextWrapping = TextWrapping.Wrap,
                FontSize     = 14
            };

            // Bold sender label — dark blue for bot, dark green for user
            tb.Inlines.Add(new Run
            {
                Text       = (isBot ? "🤖 Bot" : $"👤 {sender}") + "\n",
                FontWeight = FontWeights.Bold,
                Foreground = isBot
                             ? new SolidColorBrush(Color.FromRgb(30, 58, 95))   // dark navy
                             : new SolidColorBrush(Color.FromRgb(6, 95, 70))    // dark green
            });

            // Normal-weight message body in black for readability
            tb.Inlines.Add(new Run
            {
                Text       = message,
                Foreground = Brushes.Black
            });

            bubble.Child = tb;

            // Add bubble to the ListView and scroll it into view immediately
            chats.Items.Add(bubble);
            chats.ScrollIntoView(bubble);
        }


        /// Reads the current memory summary from the engine and updates
        // the sidebar TextBlock (memory_panel).
        // Called after every message send so the panel stays current.
        private void RefreshMemoryPanel()
        {
            string summary = _chatbot.GetMemorySummary();

            // Show a hint string if nothing has been remembered yet,
            // so the panel is never blank and confusing to the user.
            memory_panel.Text = string.IsNullOrEmpty(summary)
                                ? "Nothing remembered yet."
                                : summary;
        }
    }
