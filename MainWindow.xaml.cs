        // ── Screen 3: Chat ─────────────────────────────────────────────


        // KeyDown handler on the chat input TextBox.
        // Pressing Enter sends the message, matching the behaviour of most chat apps.
        private void question_KeyDown(object sender, KeyEventArgs e)
        {
            if (e.Key == Key.Enter)
                send(sender, e);
        }

        // Click handler for the "Send" button (also called by Enter key).
        // Reads the input, shows the user's bubble, gets the bot's reply,
        // shows the bot's bubble, increments the message counter, and
        // refreshes the memory panel.
        private async void send(object sender, RoutedEventArgs e)
        {
            string userInput = question.Text.Trim();

            // Do nothing if the input is empty or just whitespace
            if (string.IsNullOrWhiteSpace(userInput)) return;

            // 1. Show the user's message in a green bubble on the right side
            ShowMessage(_chatbot.UserName, userInput);
            question.Clear();

            // 2. PART 3: Run the NLP simulation first. If it recognises an
            //    intent (add task, set reminder, start quiz, show log, etc.),
            //    handle it directly and skip the Part 2 keyword/sentiment engine.
            //    This lets natural phrasing like "remind me to update my
            //    password tomorrow" trigger real actions from the chat box,
            //    while anything NOT recognised (e.g. "tell me about phishing")
            //    falls through unchanged to the existing Part 1/2 behaviour.
            NlpResult nlpResult = _nlp.Detect(userInput);

            if (nlpResult.Intent != NlpIntent.None)
            {
                string botReply = await HandleNlpIntentAsync(nlpResult, userInput);
                ShowMessage("Bot", botReply);
                _chatbot.IncrementMessages();
                RefreshMemoryPanel();
                return;
            }

            // 3. No NLP intent matched — fall back to the original Part 2 engine.
            EngineResponse response = _chatbot.ProcessInput(userInput);
            ShowMessage("Bot", response.Message);
            _chatbot.IncrementMessages();
            RefreshMemoryPanel();
        }

        // PART 3 — Executes the action implied by a recognised NLP intent
        // and returns the bot's chat-style reply text. Every branch logs
        // its action to ActivityLogger, matching the brief's requirement
        // that NLP-driven actions appear in the activity log.
        private async Task<string> HandleNlpIntentAsync(NlpResult result, string originalInput)
        {
            switch (result.Intent)
            {
                case NlpIntent.AddTask:
                {
                    string title = string.IsNullOrWhiteSpace(result.Detail) ? "New cybersecurity task" : result.Detail;
                    var (success, message, _) = await _taskAssistant.AddTaskAsync(title, title, null);
                    _activityLog.Log($"NLP recognised 'add task' intent from chat input", "NLP");
                    return success ? message + "\n\n(You can also manage tasks from the Tasks panel.)" : message;
                }

                case NlpIntent.SetReminder:
                {
                    // The brief's example treats "remind me to X" as adding a task
                    // WITH a reminder attached, e.g.:
                    //   "Remind me to update my password tomorrow."
                    //   -> "Reminder set for 'Update my password' on tomorrow's date."
                    string title = string.IsNullOrWhiteSpace(result.Detail) ? "Reminder" : result.Detail;
                    TimeSpan? offset = _nlp.ExtractTimeframe(originalInput);
                    DateTime reminderDate = DateTime.Now.Add(offset ?? TimeSpan.FromDays(3));

                    var (success, message, _) = await _taskAssistant.AddTaskAsync(title, title, reminderDate);
                    _activityLog.Log("NLP recognised 'set reminder' intent from chat input", "NLP");
                    return success
                        ? $"Reminder set for '{title}' on {reminderDate:dd MMM yyyy}."
                        : message;
                }

                case NlpIntent.ViewTasks:
                {
                    List<TaskItem> tasks = await _taskAssistant.GetAllTasksAsync();
                    return _taskAssistant.FormatTaskListForChat(tasks);
                }

                case NlpIntent.CompleteTask:
                {
                    TaskItem match = await _taskAssistant.FindTaskByTitleFragmentAsync(result.Detail);
                    if (match == null)
                        return "I couldn't find a matching task. Try checking the Tasks panel for exact titles.";
                    var (success, message) = await _taskAssistant.CompleteTaskAsync(match.Id, match.Title);
                    return message;
                }

                case NlpIntent.DeleteTask:
                {
                    TaskItem match = await _taskAssistant.FindTaskByTitleFragmentAsync(result.Detail);
                    if (match == null)
                        return "I couldn't find a matching task to delete. Try checking the Tasks panel for exact titles.";
                    var (success, message) = await _taskAssistant.DeleteTaskAsync(match.Id, match.Title);
                    return message;
                }

                case NlpIntent.StartQuiz:
                {
                    // Switch the user straight into the Quiz panel and start a fresh attempt,
                    // mirroring how clicking the sidebar "Quiz" button behaves.
                    nav_button_Click(nav_quiz_button, new RoutedEventArgs());
                    start_quiz_button_Click(this, new RoutedEventArgs());
                    return "Let's test your cybersecurity knowledge! I've opened the quiz for you. 🛡️";
                }

                case NlpIntent.ShowActivityLog:
                    return _activityLog.GetFormattedSummary(showAll: false);

                case NlpIntent.ShowMoreActivityLog:
                    return _activityLog.GetFormattedSummary(showAll: true);

                default:
                    return "I'm not sure how to help with that yet.";
            }
        }

