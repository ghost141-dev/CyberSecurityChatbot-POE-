            // (We're already on the UI thread here since this was awaited
            // from the constructor without ConfigureAwait(false), but being
            // explicit about Dispatcher usage is good practice for async UI code.)
            Dispatcher.Invoke(() =>
            {
                tasks_db_status_text.Text = success
                    ? ""
                    : "⚠️ Could not connect to MySQL. Make sure your local MySQL server is running.";
            });
        }

