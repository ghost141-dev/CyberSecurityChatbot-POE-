// ============================================================
// MainWindow.xaml.cs
// ------------------------------------------------------------
// Code-behind for the WPF main window.
//
// The window contains three overlapping Grid controls, each
// representing one screen in the application flow:
//
//   home_grid      Splash screen — logo + "Get Started" button.
//   username_grid  Name entry card — TextBox + "Start Chat" button.
//   chat_grid      Full chat UI — sidebar topics, message list, input bar.
//
// Screens are swapped by toggling Visibility (Hidden/Visible).
// Only one grid is Visible at a time; the others are Hidden.
//
// Chat bubbles are built entirely in code (no XAML DataTemplate)
// so each message can be individualised (colour, label, alignment)
// without a custom ViewModel or data binding.
// ============================================================

